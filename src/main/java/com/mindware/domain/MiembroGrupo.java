package com.mindware.domain;


public class MiembroGrupo {
	private Integer miembro_grupo_id;
	private Integer contacto_di;
	private Integer grupo_id;
	public Integer getMiembro_grupo_id() {
		return miembro_grupo_id;
	}
	public void setMiembro_grupo_id(Integer miembro_grupo_id) {
		this.miembro_grupo_id = miembro_grupo_id;
	}
	public Integer getContacto_di() {
		return contacto_di;
	}
	public void setContacto_di(Integer contacto_di) {
		this.contacto_di = contacto_di;
	}
	public Integer getGrupo_id() {
		return grupo_id;
	}
	public void setGrupo_id(Integer grupo_id) {
		this.grupo_id = grupo_id;
	}
	
	
	
}
